//-------------------------------------------------------
//                      求阶乘
//-------------------------------------------------------
// n! = n(n-1)(n-2) *** 3*2*1
unsigned long getFactorial(unsigned long num) {
    if (num == 1) {
        return 1;
    }
    return num * getFactorial(num-1);
}
